def run_test():

    service = ModerationService()

    tokenizer = AutoTokenizer.from_pretrained('./models/rubert-tiny-toxicity')
    model_fp32 = AutoModelForSequenceClassification.from_pretrained('./models/rubert-tiny-toxicity')

    # INT8 квантизация
    service.model = torch.quantization.quantize_dynamic(
        model_fp32,
        {torch.nn.Linear},
        dtype=torch.qint8,
    )

    service.tokenizer = tokenizer
    service.model.to(service.device)
    service.model.eval()

    size_mb = sum(p.numel() * p.element_size() for p in service.model.parameters()) / (1024 ** 2)
    print(f"Модель загружена в INT8 (~{size_mb:.1f} MB) на {service.device}")


    # убираем true_label перед отправкой в модель
    input_data = [
        {
            "post_id": x["post_id"],
            "full_text": x["full_text"]
        }
        for x in test_data
    ]

    predictions = service.analyze_text(input_data)

    evaluate(predictions, test_data)


