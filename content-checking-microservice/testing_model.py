# 2. Бакетизация по длине
# ----------------------------
def get_length_bucket(text):
    l = len(text)
    if l < 250:
        return "<250"
    elif l < 750:
        return "250-750"
    else:
        return "750+"


# ----------------------------
# 3. Оценка метрик
# ----------------------------
def evaluate(predictions, test_data):
    y_true = []
    y_pred = []

    buckets = defaultdict(lambda: {"y_true": [], "y_pred": []})

    for pred, true_item in zip(predictions, test_data):
        true_label = true_item["true_label"]
        pred_label = pred["verdict"]

        # приводим к бинарке
        y_true.append(1 if true_label != "non-toxic" else 0)
        y_pred.append(1 if pred_label != "non-toxic" else 0)

        bucket = get_length_bucket(true_item["full_text"])
        buckets[bucket]["y_true"].append(y_true[-1])
        buckets[bucket]["y_pred"].append(y_pred[-1])

    print("\n=== Общие метрики ===")
    print(classification_report(y_true, y_pred, target_names=["non-toxic", "toxic"]))

    # метрики по длине
    print("\n=== Метрики по длине текста ===")
    for bucket, vals in buckets.items():
        if len(vals["y_true"]) == 0:
            continue

        acc = accuracy_score(vals["y_true"], vals["y_pred"])
        precision, recall, f1, _ = precision_recall_fscore_support(
            vals["y_true"], vals["y_pred"], average="binary", zero_division=0
        )

        print(f"\n[{bucket}]")
        print(f"Samples: {len(vals['y_true'])}")
        print(f"Accuracy: {acc:.4f}")
        print(f"Precision: {precision:.4f}")
        print(f"Recall: {recall:.4f}")
        print(f"F1: {f1:.4f}")


